namespace Neverness.Gameplay;

/// <summary>
/// 鼠标按键枚举。
/// </summary>
public enum MouseButton
{
    /// <summary>左键。</summary>
    Left = 0,

    /// <summary>右键。</summary>
    Right = 1,

    /// <summary>中键。</summary>
    Middle = 2,

    /// <summary>侧键 1（前进）。</summary>
    Back = 3,

    /// <summary>侧键 2（后退）。</summary>
    Forward = 4
}
